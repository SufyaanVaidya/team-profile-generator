class Employee {
    constructor(person) {
        this.name = person;
        this.id = id;
        this.email = email;
        this.role = role;
    }
    getName () {
        if (this.name) {
            return this.name;
        }
    }
    getId () {
        if (this.id) {
            return this.id;
        }
    }
    getEmail () {
        if (this.email) {
            return this.email;
        }
    }
    getRole () {
        if (this.role) {
            return this.role;
        }
    }
    
}

module.exports = Employee;